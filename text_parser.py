from transformers import pipeline

summarizer = pipeline("summarization", model="facebook/bart-large-cnn")

text = """
Для решения этой задачи удобно использовать формулу для подсчёта элементов в объединении множеств. Обозначим множество учеников, которые занимаются математикой, как M, а множество учеников, которые занимаются физикой, как F. Из условия известно, что |M| = 18, |F| = 12, а пересечение |M ∩ F| = 8.  

Сначала найдём число учеников, которые занимаются как математикой, так и физикой, — это уже дано: 8 человек. Теперь узнаем, сколько учеников занимаются хотя бы одним из предметов, то есть объединением множеств M и F. Формула для объединения двух множеств такова:
|M ∪ F| = |M| + |F| - |M ∩ F|.  

Подставляя значения:
|M ∪ F| = 18 + 12 - 8 = 22.  

Это значит, что 22 ученика занимаются математикой, физикой или обоими предметами. Общее число учеников — 30. Тогда количество учеников, которые не занимаются ни математикой, ни физикой, равно:
Общее число учеников - число учеников, занимающихся хотя бы одним предметом = 30 - 22 = 8.  

Ответ: 8 учеников не занимаются ни математикой, ни физикой.  

Таким образом, подробно рассмотрев пересечения множеств и используя формулу для объединения, мы нашли искомое число.
"""


summary = summarizer(text, max_length=25, min_length=5, do_sample=False)

print("Главная мысль:", summary[0]['summary_text'])
